public class SarumanAndLanisters {

    public static void main(String[] args) {

        final int warriorsAtLanisters = 2500;
        System.out.println("Пехотинцы Ланистеров: " + warriorsAtLanisters);
        final int orksAtSaruman = 3000;
        System.out.println("Орки Сарумана: " + orksAtSaruman);
        final int armyLanistersAndSaruman = warriorsAtLanisters + orksAtSaruman;
        System.out.println("Численность войска Ланистеров и Сарумана: " + armyLanistersAndSaruman);
    }
}
